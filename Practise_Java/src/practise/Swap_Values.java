//swap using three variable
package practise;
public class Swap_Values 
{

	public static void main(String[] args) 
	{
		int a = 100;
		int b = 200;
		
		int c = a; //swap using three variables
		a = b; //updating value
		b = c;
		
		System.out.println("after swap value of a " +a);
		System.out.println("after swap value of a " +b);

	}

}
